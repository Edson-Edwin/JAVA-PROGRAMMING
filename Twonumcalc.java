public class Twonumcalc {
    public static void main(String[] args){
        float num1=12.5f;
        float num2=3.5f;
        float sum=num1+num2;
        float sub=num1-num2;
        float mul=num1*num2;
        float div=num1/num2;
        System.out.println(num1+" + "+num2+" = "+sum);
        System.out.println(num1+" - "+num2+" = "+sub);
        System.out.println(num1+" * "+num2+" = "+mul);
        System.out.println(num1+" / "+num2+" = "+div);
    }
}
